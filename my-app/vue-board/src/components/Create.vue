<template>
    <div>
        <input v-model="writer" placeholder="글쓴이"/>
        <input v-model="title" placeholder="제목"/>
        <textarea v-model="content" placeholder="내용"/>
        <button @click="write">작성</button>
    </div>    
</template>
<script>
import data from '@/data'

export default {
    name: 'Create',
    data() {
        return {
            data: data,
            writer: "",
            title: "",
            content: ""
        }
    },
    methods: {
        write(){
           this.data.push({
               writer : this.writer,
               title : this.title,
               content : this.content,
           })
           //Route : 뷰에서 화면이 전환될때 전환하는행위
           this.$router.push({
               path : '/'
           })
        }
    }
}
</script>